document.getElementById('hit_europe').addEventListener('mouseenter', function () {
  document.getElementById('europe').style.display = 'block';
});

document.getElementById('europe').addEventListener('mouseenter', function () {
  document.getElementById('europe').style.display = 'block';
});

document.getElementById('hit_europe').addEventListener('mouseleave', function () {
  document.getElementById('europe').style.display = 'none';
});

document.getElementById('europe').addEventListener('mouseleave', function () {
  document.getElementById('europe').style.display = 'none';
});